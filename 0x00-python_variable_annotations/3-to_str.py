#!/usr/bin/env python3
"""
A module that contains a function to return the string representation of a float with type annotations.
"""

def to_str(n: float) -> str:
    """
    Returns the string representation of a float.

    Parameters:
    ----------
    n : float
        The float number to be converted to a string.

    Returns:
    -------
    str
        The string representation of the given float.
    """
    return str(n)

