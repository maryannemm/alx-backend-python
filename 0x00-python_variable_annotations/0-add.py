#!/usr/bin/env python3
"""
A module that contains a function to add two float numbers with type annotations.
"""

def add(a: float, b: float) -> float:
    """
    Adds two floats and returns the result.

    Parameters:
    ----------
    a : float
        The first float number.
    b : float
        The second float number.

    Returns:
    -------
    float
        The sum of a and b.
    """
    return a + b

